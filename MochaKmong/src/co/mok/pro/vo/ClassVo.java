package co.mok.pro.vo;

public class ClassVo {
	
	private int classCode; //클래스 번호
	private String userId; //사용자ID
	private int areaCode; //지역코드
	private int timeCode; //시간코드
	private String className; //클래스명
	private String cateGoryA; //카테고리 대분류
	private String cateGoryB; //카테고리 소분류
	private String classIntroduce; //클래스소개
	private String curriculum; //커리큘럼
	private int ratingCode; //별점
	
	
	public int getClassCode() {
		return classCode;
	}
	public void setClassCode(int classCode) {
		this.classCode = classCode;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(int areaCode) {
		this.areaCode = areaCode;
	}
	public int getTimeCode() {
		return timeCode;
	}
	public void setTimeCode(int timeCode) {
		this.timeCode = timeCode;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getCateGoryA() {
		return cateGoryA;
	}
	public void setCateGoryA(String cateGoryA) {
		this.cateGoryA = cateGoryA;
	}
	public String getCateGoryB() {
		return cateGoryB;
	}
	public void setCateGoryB(String cateGoryB) {
		this.cateGoryB = cateGoryB;
	}
	public String getClassIntroduce() {
		return classIntroduce;
	}
	public void setClassIntroduce(String classIntroduce) {
		this.classIntroduce = classIntroduce;
	}
	public String getCurriculum() {
		return curriculum;
	}
	public void setCurriculum(String curriculum) {
		this.curriculum = curriculum;
	}
	public int getRatingCode() {
		return ratingCode;
	}
	public void setRatingCode(int ratingCode) {
		this.ratingCode = ratingCode;
	}
	
	
	
	
	
}
